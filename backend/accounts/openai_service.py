class UniqueResponseTracker:
    """
    Tracks responses to ensure they don't repeat or use similar phrases.
    Prevents the AI from giving the same answer twice in two months (60 days).
    """
    
    def __init__(self, user_id, time_period_days=60):
        """
        Initialize tracker for a specific user
        
        Args:
            user_id: Django user ID
            time_period_days: How many days to track responses (default: 60 days / 2 months)
        """
        self.user_id = user_id
        self.cache_key = f"response_history_{user_id}"
        self.time_period = time_period_days * 24 * 60 * 60  # Convert to seconds
    
    def get_response_history(self):
        """Retrieve all responses generated by this user in the tracking period"""
        return cache.get(self.cache_key, [])
    
    def add_response(self, response_text):
        """Add a new response to the tracking history"""
        history = self.get_response_history()
        history.append(response_text)
        cache.set(self.cache_key, history, self.time_period)
    
    def get_similar_responses(self, text):
        """
        Find previously generated responses that are similar to the current one
        Uses basic similarity checking on sentence level
        """
        history = self.get_response_history()
        sentences = text.split('.')
        
        similar = []
        for prev_response in history:
            prev_sentences = prev_response.split('.')
            for sentence in sentences:
                for prev_sentence in prev_sentences:
                    if self._is_similar(sentence.strip(), prev_sentence.strip()):
                        similar.append(prev_response)
        
        return similar
    
    @staticmethod
    def _is_similar(text1, text2, threshold=0.7):
        """
        Check if two sentences are similar using word overlap
        Returns True if similarity > threshold
        """
        if not text1 or not text2:
            return False
        
        words1 = set(text1.lower().split())
        words2 = set(text2.lower().split())
        
        if not words1 or not words2:
            return False
        
        intersection = len(words1 & words2)
        union = len(words1 | words2)
        
        similarity = intersection / union if union > 0 else 0
        return similarity > threshold


