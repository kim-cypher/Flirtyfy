def _get_time_slot(hour: int) -> str:
    if 5 <= hour < 9:
        return 'early_morning'
    if 9 <= hour < 12:
        return 'morning'
    if 12 <= hour < 14:
        return 'midday'
    if 14 <= hour < 18:
        return 'afternoon'
    if 18 <= hour < 21:
        return 'evening'
    if 21 <= hour < 24:
        return 'night'
    return 'late_night'


def _get_temporal_context(time_slot: str = None) -> dict:
    tz = ZoneInfo(getattr(settings, 'FLIRTYFY_TIMEZONE', 'America/New_York'))
    now = datetime.datetime.now(tz=tz)
    day = now.strftime('%A')
    slot = time_slot if (time_slot and time_slot in _MOODS_BY_SLOT) else _get_time_slot(now.hour)
    mood = random.choice(_MOODS_BY_SLOT[slot])
    return {
        'day':         day,
        'slot':        slot,
        'time_label':  _TIME_LABELS[slot],
        'mood':        mood,
        'day_overlay': _DAY_OVERLAYS[day],
    }


