def enforce_char_limit(text: str, max_chars: int = 150) -> str:
    """Trim at a sentence boundary if over max_chars. Never cuts mid-word."""
    text = text.strip()
    if len(text) <= max_chars:
        return text
    last_q = text.rfind('?', 0, max_chars + 1)
    if last_q > 0:
        return text[:last_q + 1]
    last_end = max(text.rfind('.', 0, max_chars + 1), text.rfind('!', 0, max_chars + 1))
    if last_end > 0:
        return text[:last_end + 1]
    truncated = text[:max_chars]
    last_space = truncated.rfind(' ')
    if last_space > 0:
        return truncated[:last_space]
    return truncated


def enforce_130_chars(text: str) -> str:
    """Backward-compatible alias used by intent_detector.py."""
    return enforce_char_limit(text, max_chars=150)


def ensure_ends_with_question(text: str, max_chars: int = 150) -> str:
    text = text.strip()
    if not text:
        return "What does that do to you?"
    if text.endswith('?'):
        return text
    if text.endswith('.'):
        candidate = text[:-1] + '?'
    elif text[-1].isalnum():
        candidate = text + '?'
    else:
        # Ends with !, ;, :, or other non-question punctuation — strip and re-add ?
        candidate = text.rstrip('.,!;: ') + '?'
    if len(candidate) > max_chars:
        truncated = candidate[:max_chars]
        last_space = truncated.rfind(' ')
        if last_space > max_chars // 2:
            truncated = truncated[:last_space]
        candidate = truncated.rstrip('.,!? ') + '?'
    return candidate


_REFUSAL_MARKERS = (
    "i need to decline", "i can't generate", "i cannot generate",
    "i'm unable", "i am unable", "i won't be able", "i appreciate the",
    "not able to", "i'd prefer not", "i must decline", "i cannot write",
    "i can't write", "i won't write", "generating this content",
    "this request", "i need to respectfully",
)


def _is_refusal(text: str) -> bool:
    t = text.lower()
    return any(m in t for m in _REFUSAL_MARKERS)


# ---------------------------------------------------------------------------
# Character-break detector — shared by both surfaces (left panel + buttons).
# Catches AI self-disclosure, meta-commentary about the conversation itself,
# or explicit capability statements ("what I can do") that break persona.
# Defined here (the base module) rather than in intent_detector.py, since
# intent_detector.py already imports from this module — keeping it here
# avoids a circular import.
# ---------------------------------------------------------------------------

_CHARACTER_BREAK_PATTERN = re.compile(
    r'\bI(?:\'m|\s+am)\s+(?:Claude|an?\s+AI|an?\s+artificial\s+intelligence|a\s+language\s+model)\b'
    r'|\bmade\s+by\s+Anthropic\b'
    r'|\bI\s+need\s+to\s+(?:pause|stop|step\s+out|be\s+direct|name\s+(?:this|what))\b'
    r'|\bI\s+can(?:\'t|not)\s+(?:write|continue|roleplay|engage|participate|respond\s+to)\b'
    r'|\bI\s+need\s+to\s+be\s+direct\s+with\s+you\b'
    r'|\bThis\s+conversation\s+(?:shows|has|isn\'t|doesn\'t)\b'
    r'|\bstep\s+out\s+of\s+character\b'
    r'|\bAs\s+an\s+AI\b'
    r'|\bI\'m\s+not\s+a\s+real\s+woman\b'
    r'|\bI\s+cannot\s+(?:pretend|impersonate|continue\s+this)\b'
    r'|\b(?:that\'?s|this\s+is)\s+outside\s+what\s+I\s+(?:can|do|will)\b'
    r'|\bwhat\s+I\s+\*?can\*?\s+do\s+(?:is|instead)\b',
    re.IGNORECASE,
)


def _has_character_break(text: str) -> bool:
    return bool(_CHARACTER_BREAK_PATTERN.search(text))


def validate_character_voice(text: str) -> str:
    """
    Strip AI-assistant phrases, em-dashes, and the 'actually' verbal tic
    that bleeds through even when the system prompt bans it.
    """
    ai_phrases = [
        "As an AI", "I'm an AI", "As an assistant", "I can help",
        "Feel free to", "Let me know", "I'd be happy to",
        "I'm here to", "I'm designed to", "Here's what I think",
    ]
    for phrase in ai_phrases:
        text = text.replace(phrase, '').replace(phrase.lower(), '')

    # Em-dashes → period + space
    text = re.sub(r'[.!?]?\s*—\s*', '. ', text)
    text = re.sub(r'\.\s+([a-z])', lambda m: '. ' + m.group(1).upper(), text)

    # Strip 'actually' — single most common AI verbal tic; system prompt isn't enough alone
    text = re.sub(r'\bactually\b,?\s*', '', text, flags=re.IGNORECASE)

    return ' '.join(text.split()).strip()


_CONTACT_LEAK_PATTERNS = re.compile(
    r'\b(hear|hearing)\s+(your|his|their)\s+voice\b'
    r'|\bwant\s+to\s+(call|hear)\b'
    r'|\b(call|facetime|video\s+call)\s+(me|you|us|each\s+other)\b'
    r'|\bover\s+the\s+phone\b'
    r'|\bphone\s+call\b'
    r'|\bvoice\s+(note|memo|message|call)\b',
    re.IGNORECASE,
)


def _has_contact_leak(text: str) -> bool:
    return bool(_CONTACT_LEAK_PATTERNS.search(text))


