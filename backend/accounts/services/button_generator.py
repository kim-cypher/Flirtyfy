# ---------------------------------------------------------------------------
# Rescue question generation — used only when both primary generation
# attempts fail validation. Instead of grabbing a canned string (which is
# exactly the kind of literal repeated text that gets flagged), this makes
# one more tightly-scoped, cheap LLM call asking for a single original
# question in the right category/word, explicitly told to avoid resembling
# recent output. This is a plain instruction-following task (no character
# voice, no explicit content judgment), so it essentially never refuses.
# ---------------------------------------------------------------------------

def _rescue_question(category: dict, question_word: str, avoid_texts: list) -> str | None:
    avoid_snippet = ''
    if avoid_texts:
        sample = avoid_texts[-5:]
        avoid_snippet = (
            "\n\nDo not resemble these recently used messages in wording or structure:\n"
            + '\n'.join(f'- {t}' for t in sample)
        )
    prompt = (
        f"Write ONE original question a woman would text a man on a dating app.\n"
        f"10-16 words. Must start with the word \"{question_word}\".\n"
        f"Category: {category['label']} — {category['instruction']}"
        f"{avoid_snippet}\n\n"
        "Output only the question itself, nothing else, ending with a question mark."
    )
    try:
        resp = get_anthropic_client().messages.create(
            model='claude-haiku-4-5',
            system=_RETRY_SYSTEM,
            messages=[{'role': 'user', 'content': prompt}],
            temperature=1.0,
            max_tokens=40,
        )
        q = resp.content[0].text.strip().rstrip('.,!;: ')
        return q if q.endswith('?') else q + '?'
    except Exception as e:
        logger.error(f"Rescue question generation failed: {e}")
        return None


_CHARACTER_BREAK_LAST_RESORT = "I'm not thinking about any of that right now. What's been on your mind today?"


def _character_break_fallback(user_id: int, button_intent: str, time_slot: str = None) -> str:
    """
    Handles a character break detected inside generate_button_response itself.
    Routes through the 'vulnerability' button's own pipeline — same mechanism
    already used by the left panel's deflection. Guarded against recursion:
    if 'vulnerability' itself is the one that broke character, falls straight
    to a single static line instead of calling itself again.
    """
    if button_intent == 'vulnerability':
        logger.warning(f"Character break on vulnerability button itself — user:{user_id}, using static fallback")
        return _CHARACTER_BREAK_LAST_RESORT
    fallback_result = generate_button_response(user_id, 'vulnerability', time_slot=time_slot)
    return fallback_result.get('response', _CHARACTER_BREAK_LAST_RESORT)


