# ---------------------------------------------------------------------------
# Rescue question generation — used only when both primary generation
# attempts fail validation. Instead of grabbing a canned string (which is
# exactly the kind of literal repeated text that gets flagged), this makes
# one more tightly-scoped, cheap LLM call asking for a single original
# question in the right category/word, explicitly told to avoid resembling
# recent output. This is a plain instruction-following task (no character
# voice, no explicit content judgment), so it essentially never refuses.
# ---------------------------------------------------------------------------

def _rescue_question(category: dict, question_word: str, avoid_texts: list) -> str | None:
    avoid_snippet = ''
    if avoid_texts:
        sample = avoid_texts[-5:]
        avoid_snippet = (
            "\n\nDo not resemble these recently used messages in wording or structure:\n"
            + '\n'.join(f'- {t}' for t in sample)
        )
    prompt = (
        f"Write ONE original question a woman would text a man on a dating app.\n"
        f"10-16 words. Must start with the word \"{question_word}\".\n"
        f"Category: {category['label']} — {category['instruction']}"
        f"{avoid_snippet}\n\n"
        "Output only the question itself, nothing else, ending with a question mark."
    )
    try:
        resp = get_anthropic_client().messages.create(
            model=settings.ANTHROPIC_REWRITE_MODEL,
            system=_RETRY_SYSTEM,
            messages=[{'role': 'user', 'content': prompt}],
            temperature=1.0,
            max_tokens=40,
        )
        q = resp.content[0].text.strip().rstrip('.,!;: ')
        return q if q.endswith('?') else q + '?'
    except Exception as e:
        logger.error(f"Rescue question generation failed: {e}")
        return None


_CHARACTER_BREAK_LAST_RESORT = "I'm not thinking about any of that right now. What's been on your mind today?"


def _character_break_fallback(user_id: int, button_intent: str, time_slot: str = None) -> str:
    """
    Handles a character break detected inside generate_button_response itself.
    Routes through the 'vulnerability' button's own pipeline — same mechanism
    already used by the left panel's deflection. Guarded against recursion:
    if 'vulnerability' itself is the one that broke character, falls straight
    to a single static line instead of calling itself again.
    """
    if button_intent == 'vulnerability':
        logger.warning(f"Character break on vulnerability button itself — user:{user_id}, using static fallback")
        return _CHARACTER_BREAK_LAST_RESORT
    fallback_result = generate_button_response(user_id, 'vulnerability', time_slot=time_slot)
    return fallback_result.get('response', _CHARACTER_BREAK_LAST_RESORT)


# ---------------------------------------------------------------------------
# Response generation
# ---------------------------------------------------------------------------

def generate_button_response(user_id: int, button_intent: str, time_slot: str = None) -> dict:
    """
    Generate one message for the given button.

    Redis session per user (24h TTL) tracks:
      - used_themes per button_intent
      - opener_style_{intent} per button (rotation over 4 abstract styles — never literal text)
      - q_cats_{intent}, tone_{intent}, q_word_{intent} per button

    Phrase-level uniqueness (literal 4-gram repetition) is checked against the
    user's last 30 days of replies in the DB (see dedup.py) — not Redis — so
    the window matches the actual 30-day retention and survives restarts.

    Returns dict with 'response', 'theme', 'button_intent'.
    Returns dict with 'error' key on failure.
    """
    if button_intent not in BUTTON_INTENTS:
        return {'error': f'Unknown button intent: {button_intent}'}

    session_key = f"user_btn_{user_id}"
    session_data = cache.get(session_key) or {'used_themes': {}}
    session_data.setdefault('used_themes', {})

    used_themes = session_data['used_themes'].get(button_intent, [])

    # ── Step 1: Select all rotated parameters ──────────────────────────────
    opener_style    = _select_opener_style(button_intent, session_data)
    category        = _select_question_category(button_intent, session_data)
    tone            = _select_tone(button_intent, session_data)
    question_word   = _select_question_word(button_intent, session_data)

    # ── Step 2: Build prompt ───────────────────────────────────────────────
    intent_config = BUTTON_INTENTS[button_intent]
    user_prompt = intent_config['prompt']

    # Row 7 ("getting to know him") buttons ask her to share something real
    # about her own life first, with no fixed subject — see _LIFE_SLICES above
    # for why this is scoped to row 7 only.
    if intent_config['row'] == 7:
        life_slice = _select_life_slice(button_intent, session_data)
        user_prompt += (
            f'\n\nWhat she shares must be grounded in this concrete territory: '
            f'{life_slice["name"].upper()} — {life_slice["instruction"]} '
            f'Invent your own specific real detail in this territory — never reuse a phrase from a previous message.'
        )

    if button_intent == 'new_match':
        angle = _select_new_match_angle(session_data)
        user_prompt += (
            f'\n\nWhat she noticed must be grounded in this specific angle: '
            f'{angle["name"].upper()} — {angle["instruction"]} '
            f'Invent your own specific detail in this angle — never reuse a phrase from a previous message. '
            f'Never use the word "profile" or "bio" — respond to what he actually said, not an abstract profile.'
        )

    if used_themes:
        user_prompt += (
            f"\n\nThemes already used — DO NOT repeat any of these: {', '.join(used_themes)}. "
            f"Pick a completely different emotional angle, physical state, and question direction. "
            f"If the last reply was explicit, go emotional. If emotional, go playful. Force real contrast."
        )

    # Recent-outputs avoid-list — the buttons used to see only coarse THEME
    # tags, so the model kept reusing the same opening ("I was scrolling…",
    # "my <body part> reading that…"). Showing it the actual recent messages,
    # and their opening structures explicitly, is what makes every click feel
    # new. Pure DB read (indexed) — negligible cost.
    recent = get_recent_user_texts(user_id)[:5]
    if recent:
        recent_openers = {_opener_signature(t) for t in recent if t}
        user_prompt += (
            "\n\nHer OWN recent messages — your reply must not resemble any of them in "
            "opening words, sentence shape, imagery, or question:\n"
            + "\n".join(f"- {t}" for t in recent)
        )
        if recent_openers:
            user_prompt += (
                "\nShe has recently opened messages with these structures — open in a "
                "completely different way this time, do not start with the same idea:\n"
                + "; ".join(sorted(recent_openers))
            )

    # Temporal context: mood + overlay only. Never pass the literal day name
    # or time label — when the prompt said "It is Friday, late night", the
    # model NAMED the day/time in output ("Friday night", "midnight").
    temporal = _get_temporal_context(time_slot=time_slot)
    user_prompt += (
        f"\n\nHer mood right now (texture only — never name any day or time): "
        f"{temporal['mood']}, {temporal['day_overlay']}."
    )

    register_line = _ROW_REGISTER.get(intent_config.get('row'), _ROW_REGISTER[1])
    user_prompt += f"\n\nRegister for this button: {register_line}"

    user_prompt += (
        f'\n\nTone: {tone.upper()} — {_TONE_DESCRIPTIONS[tone]}\n'
        f'Both sentences carry this tone in word choice, pacing, and temperature.'
    )

    user_prompt += (
        f'\n\nSentence 2 question category: {category["label"].upper()}\n'
        f'{category["instruction"]}\n'
        f'Write your own completely original question in this category — '
        f'the question must grow from what sentence 1 just said, not be generic. '
        f'He must answer from a real memory, decision, physical feeling, or honest truth. Never yes or no.\n'
        f'REQUIRED: The question MUST begin with the word "{question_word}" — no other starter is acceptable.'
    )

    is_night = temporal['slot'] in ('night', 'late_night')
    btn_max_tokens = 90 if is_night else 110
    btn_max_chars  = 210 if is_night else 300

    user_prompt += (
        f'\n\nOpening style: {opener_style["name"].upper()} — {opener_style["instruction"]} '
        f'Never reuse a phrase from a previous message. Avoid generic filler like "Honestly," '
        f'or "Real talk," — invent something specific to this exact message.'
        f'\n\nLength: under {btn_max_chars} characters total. Short, common words only.'
    )

    # ── Step 3: Generate ───────────────────────────────────────────────────
    try:
        response = get_anthropic_client().messages.create(
            model=settings.ANTHROPIC_GENERATION_MODEL,
            # Static system prompt is marked cacheable; the rotating user
            # prompt stays after the cache breakpoint.
            system=[{
                'type': 'text',
                'text': _SYSTEM,
                'cache_control': {'type': 'ephemeral'},
            }],
            messages=[{'role': 'user', 'content': user_prompt}],
            # Sonnet 5 rejects non-default temperature/top_p — never pass them.
            thinking={'type': 'disabled'},
            max_tokens=btn_max_tokens,
        )

        result = next(
            (b.text for b in response.content if getattr(b, 'type', '') == 'text'), ''
        ).strip()

        # ── Output gate: catch character breaks / AI disclosure ────────────────
        if _has_character_break(result):
            logger.warning(f"Button character break detected — user:{user_id} intent:{button_intent}")
            return {
                'response': _character_break_fallback(user_id, button_intent, time_slot),
                'theme': 'character_break_fallback',
                'button_intent': button_intent,
            }

        result = enforce_char_limit(result, max_chars=btn_max_chars)
        result = validate_character_voice(result)
        result = ensure_ends_with_question(result, max_chars=btn_max_chars)

        def _has_two_sentences(t: str) -> bool:
            """Exactly 2 sentences: one statement, one question."""
            parts = re.split(r'(?<=[.!?])\s+', t.strip())
            return len(parts) == 2

        def _result_is_bad(t: str) -> bool:
            return (
                _is_refusal(t)
                or not _is_genuine_question(t)
                or not _has_two_sentences(t)
                or _has_contact_leak(t)
                or _has_physical_reality_intrusion(t)
                or _has_perception_leak(t)
                or _has_meeting_fantasy(t)
                or _has_male_anatomy_language(t)
                or _has_formula_phrase(t)
                or _has_temporal_leak(t)
                or _has_time_mention(t)
                or _has_logistics_leak(t)
            )

        if _result_is_bad(result):
            retry_prompt = (
                f"Write a short text message a woman sends to a man she is attracted to on a dating app.\n\n"
                f"Context: {intent_config['prompt']}\n\n"
                f"Opening style: {opener_style['instruction']} Invent your own original opening words — "
                f"never a stock phrase, never reused from a previous message.\n\n"
                "Exactly two sentences, under 30 words total.\n"
                "Sentence 1: something specific she is doing, thinking, or feeling in her own body or mind right now. "
                "No shared physical spaces. No eye contact or being near him. Pure inner experience.\n"
                f"Sentence 2: a direct question starting with the word \"{question_word}\" — "
                "his memory, his habit, or what he would do.\n"
                f'REQUIRED: The question MUST begin with "{question_word}".\n'
                "End with a question mark. Just write the message."
            )
            retry_resp = get_anthropic_client().messages.create(
                model=settings.ANTHROPIC_GENERATION_MODEL,
                system=_RETRY_SYSTEM,
                messages=[{'role': 'user', 'content': retry_prompt}],
                thinking={'type': 'disabled'},
                max_tokens=btn_max_tokens,
            )
            retry_result = next(
                (b.text for b in retry_resp.content if getattr(b, 'type', '') == 'text'), ''
            ).strip()

            # ── Output gate on retry too ────────────────────────────────────────
            if _has_character_break(retry_result):
                logger.warning(f"Button character break on retry — user:{user_id} intent:{button_intent}")
                return {
                    'response': _character_break_fallback(user_id, button_intent, time_slot),
                    'theme': 'character_break_fallback',
                    'button_intent': button_intent,
                }

            retry_result = enforce_char_limit(retry_result, max_chars=btn_max_chars)
            retry_result = validate_character_voice(retry_result)
            retry_result = ensure_ends_with_question(retry_result, max_chars=btn_max_chars)

            if not _result_is_bad(retry_result):
                result = retry_result
            else:
                # Both attempts failed validation — rescue: regenerate ONLY the question
                # with a tightly-scoped LLM call instead of grabbing a canned string.
                # This is what actually prevents repeated text from reaching the user.
                better = (
                    retry_result
                    if not (_has_contact_leak(retry_result) or _has_physical_reality_intrusion(retry_result))
                    else result
                )
                parts = re.split(r'(?<=[.!?])\s+', better.rstrip('?').rstrip())
                s1 = parts[0].rstrip('.!,') if parts else better.rstrip('?.!,')
                # This rescue path only regenerates the QUESTION below — s1 ships
                # verbatim from whichever attempt failed validation. Without this
                # check, a formula-phrase sentence 1 (e.g. "I keep replaying...")
                # that triggered the retry in the first place would still ship,
                # just with a fresh question bolted onto it.
                if _is_refusal(s1) or len(s1.split()) < 4 or _has_formula_phrase(s1) or _has_temporal_leak(s1):
                    s1 = "Something has been on my mind"

                rescue_q = _rescue_question(
                    category, question_word,
                    avoid_texts=get_recent_user_texts(user_id)[:5],
                )
                if rescue_q:
                    result = s1 + '. ' + rescue_q
                else:
                    # True last resort — only reachable if the API itself is unavailable.
                    logger.warning(
                        f"Rescue generation exhausted — user:{user_id} intent:{button_intent}"
                    )
                    result = s1 + '. ' + 'What is on your mind about this right now?'

        # ── Step 3.5: Phrase-level uniqueness — DB-backed, 30-day window ───────
        # Single place that checks/fixes literal repetition, regardless of
        # whether `result` came from the first attempt, the retry, or rescue.
        result, was_rewritten = dedupe_against_history(get_anthropic_client(), user_id, result)
        if was_rewritten:
            logger.info(f"Dedup rewrite applied — user:{user_id} intent:{button_intent}")

        # ── Step 3.6: Near-duplicate check — pg_trgm trigram similarity ────────
        # Catches heavy rewordings the 4-gram pass misses. Pure Postgres,
        # no external API dependency.
        result, was_sim_rewritten = dedupe_similar(get_anthropic_client(), user_id, result)
        if was_sim_rewritten:
            logger.info(f"Similarity rewrite applied — user:{user_id} intent:{button_intent}")

        # ── Step 3.7: Question-tail uniqueness ──────────────────────────────────
        # The final question is where customers notice repetition first —
        # identical tails were shipping under different buttons.
        result, was_tail_rewritten = dedupe_question_tail(get_anthropic_client(), user_id, result)
        if was_tail_rewritten:
            logger.info(f"Question-tail rewrite applied — user:{user_id} intent:{button_intent}")

        # ── Step 4: Update Redis session ───────────────────────────────────
        theme = extract_theme(result)
        session_data['used_themes'][button_intent] = (used_themes + [theme])[-8:]
        cache.set(session_key, session_data, 86400)

        logger.info(
            f"Button response — user:{user_id} intent:{button_intent} "
            f"opener_style:{opener_style['name']} theme:{theme}"
        )

        return {
            'response': result,
            'theme': theme,
            'button_intent': button_intent,
        }

    except RateLimitError:
        logger.error(f"Rate limit for user {user_id}")
        return {'error': 'API rate limit hit — please wait a moment and try again.'}
    except APIConnectionError:
        logger.error(f"Connection error for user {user_id}")
        return {'error': 'Connection error with AI service — please try again.'}
    except APIError as e:
        logger.error(f"API error for user {user_id}: {e}")
        return {'error': f'AI service error: {e}'}
    except Exception as e:
        logger.error(f"Unexpected error for user {user_id}: {e}")
        return {'error': f'Server error: {e}'}


